package it.polito.tdp.anagrammi.model;

public class Parola {
	private String nome;
	private boolean esiste;
	
	public Parola(String nome, boolean esiste) {
		this.nome = nome;
		this.esiste = esiste;
	}

	public boolean esiste() {
		return esiste;
	}

	@Override
	public String toString() {
		return nome;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((nome == null) ? 0 : nome.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Parola other = (Parola) obj;
		if (nome == null) {
			if (other.nome != null)
				return false;
		} else if (!nome.equals(other.nome))
			return false;
		return true;
	}
	
	
}
